#include "pushswap_bonus.h"

void	rra(t_list *d)
{
	int	temp;
	int	i;

	temp = d->sa[d->sia - 1];
	i = d->sia - 1;
	while (--i >= 0)
		d->sa[i + 1] = d->sa[i];
	d->sa[0] = temp;
}
