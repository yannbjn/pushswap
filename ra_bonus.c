#include "pushswap_bonus.h"

void	ra(t_list *d)
{
	int	temp;
	int	i;

	temp = d->sa[0];
	i = -1;
	while (++i < (d->sia - 1))
		d->sa[i] = d->sa[i + 1];
	d->sa[d->sia - 1] = temp;
}
