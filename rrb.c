#include "pushswap.h"

void	rrb(t_list *d)
{
	int	i;

	i = d->sib - 1;
	while (i - 1 >= 0)
	{
		swap(&d->sb[i], &d->sb[i - 1]);
		i--;
	}
	write(1, "rrb\n", 4);
}
